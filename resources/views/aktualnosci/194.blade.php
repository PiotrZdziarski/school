<p>Uczniowie klasy 1b, 2b i innych klas naszej szkoły zainteresowanych informatyką i fizyką na poziomie rozszerzonym
    oraz grupa zaproszonych gimnazjalistów z klas trzecich ze Szkoły Podstawowej nr 2 w Łowiczu uczestniczyli w dniu 13
    czerwca 2018 r. w zajęciach warsztatowych z druku 3D. Absolwent naszego liceum Pan inż. Michał Staniszewski
    prezentował metody druku 3D, przedstawił wprowadzenie do modelowania przestrzennego, omówił przygotowanie wydruku i
    zasady eksploatacji drukarek 3D. Wspólnie z drugim prelegentem, Panem inż. Mateuszem Rygorczukiem ilustrowali wykład
    pokazami działania dwóch drukarek 3D.</p>

<p>Wykładowcy przedstawili zalety druku 3D: minimalizacja kosztów, niewielka ilość odpadów, jednostkowa produkcja
    elementów. Możliwy jest druk od miniaturowych protez do gotowych przekładni zębatych i domów mieszkalnych.
    Poznaliśmy materiały stosowane w druku 3D, zapis przykładowego programu i kodu maszynowego takiej drukarki, Pan inż.
    Michał Staniszewski, nasz absolwent, zaprezentował możliwości drukarek 3D i odpowiadał na pytania młodzieży.</p>

<p>Bardzo dziękujemy Panom inżynierom Michałowi Staniszewskiemu i Mateuszowi Rygorczukowi za interesujące informacje i
    pokazy. Mogliśmy z bliska obejrzeć działanie drukarek 3D i zadań własne pytania.</p>

<p>W imieniu uczniów, nauczyciela informatyki p. Jolanty Rybickiej, nauczyciela fizyki p. Marka Rybickiego i dyrekcji I
    Liceum Ogólnokształcącego im. Józefa Chełmońskiego w Łowiczu serdecznie dziękujemy Panu inż. Michałowi
    Staniszewskiemu za przeprowadzenie zajęć warsztatowych z druku 3D. Mamy nadzieję, że to spotkanie młodzieży z Naszym
    Absolwentem będzie owocować dalszą współpracą z naszą szkołą.</p>



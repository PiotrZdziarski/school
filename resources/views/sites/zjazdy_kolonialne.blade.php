                                        <div class="col-lg-12 mx-auto paddingBottom-40 padding20lowwidth padding-30 paddingTop-5"
                                             style="margin-top: -5px;">
                                            <h4 class="paddingTop-30 paddingBottom-10">Przed i w trakcie I Zjazdu
                                                Koleżeńskiego (1919)</h4>

                                            <p>Pierwszy zjazd, o którym zachowała się informacja odbył się w 1890 roku
                                                dla uczczenia jubileuszu pięćdziesięciolecia pracy znakomitego pedagoga
                                                Jana Pankiewicza, który był nauczycielem matematyki, inspektorem byłego
                                                gimnazjum realnego, a w końcu jego rektorem. Zachował się w pamięci
                                                wychowanków jako wspaniały wykładowca i wychowawca.</p>

                                            <p>Z tej samej okazji odbył się zjazd w 1901 roku. Był on równie uroczyście
                                                obchodzony jak poprzedni. Zgromadził liczną rzeszę wychowanków.</p>

                                            <p>27 i 28 września 1903 roku zgromadziła się znaczna liczba wychowanków
                                                Jana Pankiewicza. W skład Komitetu zjazdowego weszli: Antoni Alimowicz,
                                                prof. Maksymilian Białowiejski, prof. Józef Jerzy Bogucki, Kazimierz
                                                Hordliczka, dr Stanisław Kurtz, Stefan Lilpop, Stanisław Manduk, Gustaw
                                                Martens, Józef Mucharski, Stanisław Rutkowski i Józef Włoskiewicz.
                                                Sekretarzem Komitetu wybrano Józefa Mucharskiego.</p>

                                            <p>W Księdze Pamiątkowej Muzeum Starożytności w Łowiczu pod datą 29 czerwca
                                                1906 roku znajduje się zapis: „Pierwszy zjazd koleżeński b. Wychowańców
                                                Szkoły Realnej w Łowiczu”. Widnieje tam również 10 podpisów. Na
                                                podstawie tej samej Księgi można wywnioskować, że zjazdy odbyły się w
                                                1903 roku, 18 czerwca 1911 roku, 1 lipca 1913 roku. Biorąc pod uwagę
                                                małą liczbę uczestników, można przyjąć, że były to spotkania
                                                koleżeńskie.</p>

                                            <p>Na spotkanie koleżeńskie byłych wychowańców szkoły realnej z 1902 roku,
                                                które odbyło się 18 czerwca 1911 roku z liczby 22 stawiło się siedmiu:
                                                inż. Ludwik Henikowski, inż. Tomasz Kujawski, inż. Teodor Markiewicz,
                                                inż. Feliks Stępowski, inż. Walenty Wrzesiński, dyplomowany agronom
                                                Teodor Szmidecki i handlowiec Wojciech Kosiński. Zwiedzono między innymi
                                                skarbiec kolegiacki i Muzeum Starożytności, gdzie dokonano pamiątkowego
                                                wpisu. Następne spotkanie zaplanowano na 18 czerwca 1916 roku. Z uwagi
                                                na działania wojenne nie doszło ono do skutku.</p>

                                            <p>W dniach 28 i 29 czerwca 1914 roku odbył się zjazd koleżeński byłych
                                                wychowańców szkoły realnej w Łowiczu, którzy ukończyli ją w 1904 roku.
                                                Na zjazd przybyło 16 uczestników. Po zwiedzeniu miasta i jego zabytków
                                                absolwenci udali się na wycieczkę do wsi Bocheń, gdzie miło i wesoło
                                                spędzili czas. Niepogoda następnego dnia nie pozwoliła zwiedzić pałacu w
                                                Nieborowie i parku w Arkadii. Następny zjazd zaplanowano na pierwszy
                                                dzień Zielonych Świąt w 1919 roku. Na podstawie informacji prasowych
                                                wiadomo o organizowaniu zjazdu koleżeńskiego w zaplanowanym terminie. We
                                                wspomnianej Księdze Pamiątkowej pod datą 9 czerwca 1919 roku widnieje
                                                wpis: „XV Zjazd Koleżeński b. Wychowańców Łowickiej Szkoły Realnej” wraz
                                                z siedmioma podpisami. Następny zaplanowano na 1924 rok. Zjazd z 1919
                                                roku, jako pierwszy w odrodzonej Polsce, oznaczono numerem I i od tego
                                                momentu numerowano zjazdy.</p>


                                            <h4 class="paddingTop-40 paddingBottom-10">II Zjazd Koleżeński (1922)</h4>

                                            <p>Chęć kontaktu ze szkołą była tak silna, iż w dniach 4 i 5 czerwca 1922
                                                roku zorganizowano zjazd uczniów byłej szkoły realnej w Łowiczu z okresu
                                                od roku 1874 do 1905, epoki pamiętnego strajku młodzieży polskiej.
                                                Spotkanie rozpoczęło się w niedzielne popołudnie w sali Stowarzyszenia
                                                Techników w Warszawie. Zebrało się około 150 osób. Najwięcej było
                                                inżynierów. Byli jednak również duchowni, wojskowi, profesorowie
                                                wyższych uczelni, przemysłowcy i rolnicy. Przybyłych w imieniu Komitetu
                                                Organizacyjnego powitał profesor Politechniki Warszawskiej Mieczysław
                                                Bronikowski. W skład Komitetu weszli: adwokat Lucjan Parzyński, inż.
                                                Wacław Gaładyk, Stanisław Żarski, Kazimierz Toporski i Władysław
                                                Łopiński. Powołano prezydium zebrania, złożone z przewodniczącego Józefa
                                                Nehringa, członków: jenerała Czesława Rybińskiego, prof. Mieczysława
                                                Bronikowskiego, adwokata Lucjana Parzyńskiego i sekretarza Władysława
                                                Łopińskiego.
                                                Zorganizować Koło b. Wychowańców łowickiej szkoły realnej o szerokich
                                                celach społecznych i towarzyskich
                                                Opracować i wydać monografię szkoły
                                                Roztoczyć opiekę Koła nad obecną już polską szkołą w Łowiczu</p>

                                            <p>Realizacja punktu trzeciego – doraźnie urządzona zbiórka – przyniosła
                                                milion marek, które doręczono w dniu następnym dyrektorowi szkoły
                                                Władysławowi Olszewskiemu. Po zakończeniu zebrania, uczestnicy zasiedli
                                                do wspólnej biesiady. W miłym nastroju spędzili czas, a zabawa trwała do
                                                późnych godzin wieczornych.</p>

                                            <p>Nazajutrz rano wszyscy udali się koleją do Łowicza, gdzie grono zebranych
                                                powiększyło się jeszcze o kilkudziesięciu kolegów. Drugi dzień Zjazdu
                                                rozpoczęto nabożeństwem w dawnej kaplicy szkolnej. Mszę celebrował
                                                prałat Kapituły Łowickiej ks. Stępowski w asyście księży Napieralskiego
                                                i Kalinowskiego. Podczas mszy chór kolegów i orkiestra Seminarium
                                                Nauczycielskiego wykonała odpowiednie pieśni religijne. Po skończonej
                                                mszy wygłosił w tejże kaplicy gorące przemówienie do kolegów uczestnik
                                                Zjazdu ks. Napierski, nawołując ich do jedności, nie ustawania w pracy
                                                społecznej i wychowania przyszłych pokoleń w duchu narodowym i
                                                obywatelskim.</p>

                                            <p>> W gmachu szkolnym serdecznie powitał zebranych dyrektor Władysław
                                                Olszewski. Przypomniał uczestnikom ciężkie chwile spędzone we wrogiej
                                                szkole rosyjskiej, która jednakże nie potrafiła zabić ducha w młodzieży
                                                polskiej swym deprawującym systemem wychowawczym, lecz przeciwnie,
                                                zaprawiła ją do walki o wolną Ojczyznę.</p

                                            <p>Po zwiedzeniu gmachu szkoły wykonano wspólną fotografię. Następnie
                                                zwiedzono Muzeum im. Władysława Tarczyńskiego. Przechadzka po Łowiczu i
                                                wspólna biesiada na korytarzu dawnej ALMAE MATRIS zakończyła ten
                                                pamiętny zjazd koleżeński, który na wszystkich uczestnikach wywarł
                                                niezatarte wrażenie.</p>


                                            <h4 class="paddingTop-40 paddingBottom-10">III Zjazd Koleżeński (1927)</h4>

                                            <p>W 1924 roku minęło 10 lat od chwili wypuszczenia w świat ostatniego
                                                zastępu maturzystów przez b. Szkołę Realną w Łowiczu. W Warszawie
                                                utworzył się Komitet Organizacyjny Zjazdu Koleżeńskiego Maturzystów i
                                                byłych uczniów klas wyższych tej szkoły z lat 1912, 1913, 1914. Na czele
                                                Komitetu stanął Włodzimierz Łuczyński. Inne źródła podają, że w maju
                                                1924 roku Komitet Organizacyjny pod honorowym przewodnictwem ks.
                                                Maksymiliana Cichockiego odbywał w Warszawie swe zebrania, na których
                                                omawiano szczegóły organizacyjne. Wysłano około 100 zaproszeń.</p>

                                            <p>Zjazd odbył się 29 czerwca 1924 roku. Większość profesorów i kolegów klas
                                                starszych nie przyjechała, wielu przesłało depesze lub listy. Obecnych
                                                było około pięćdziesięciu osób. Punktualnie o godzinie 10.15 koledzy
                                                wszystkich wyznań udali się do kościoła, gdzie w powadze i skupieniu
                                                wysłuchano nabożeństwa, celebrowanego przez ks. Maksymiliana
                                                Cichockiego. Wygłosił on okolicznościowe kazanie. Zaakcentował ideę
                                                braterstwa i miłości. Przypomniał chwile, gdy niepodległość była tylko
                                                marzeniem, a młodzież musiała się uczyć w obcej dla siebie szkole.</p>

                                            <p>Po nabożeństwie wszyscy przeszli do szkoły, gdzie w ścisłym gronie
                                                profesorów i kolegów rozpoczęto obrady. Jako pierwszy głos zabrał
                                                Włodzimierz Łuczyński. Następnie na przewodniczącego obrano Jana
                                                Jankowskiego. Posiedzenie miało charakter podniosły i poważny. Uczczono
                                                przez powstanie zmarłych profesorów i kolegów. M. Zytner, student
                                                filologii Uniwersytetu Warszawskiego, wygłosił referat p.t. „Stosunki w
                                                dawnej szkole rosyjskiej”. Drugi referent, mjr Karpiński, ze względów
                                                służbowych spóźnił się i nie wygłosił referatu o lotnictwie.</p>

                                            <p>Z wniosków, które przyjęto na posiedzeniu, na uwagę zasługują dwa:
                                                wniosek o zostawienie pamiątki uczniom Gimnazjum im Ks. Józefa
                                                Poniatowskiego w postaci zakupienia książek do biblioteki, drugi o
                                                zapisywanie się na członków rzeczywistych i wspierających do
                                                Akademickiego Koła Łowiczan.</p>

                                            <p>Na zakończenie podziękowano Komitetowi Organizacyjnemu za pracę. Po
                                                obradach, na dziedzińcu szkolnym wykonano wspólną fotografię. Po
                                                obiedzie i krótkim odpoczynku o godzinie 16.30 na wozach bezinteresownie
                                                wypożyczonych przez mieszkańców miasta udano się na majówkę do Arkadii.
                                                Zjazd zakończyła wspólna kolacja w lokalu Klubu Urzędniczo –
                                                Obywatelskiego. Sympatyczny nastrój urozmaicany był grą na fortepianie i
                                                skrzypcach oraz deklamacjami i piosenkami przybyłych na Zjazd
                                                kolegów.</p>

                                            <p>Zjazd, aczkolwiek niezbyt liczny, pozostawił miłe wrażenie. Biorąc pod
                                                uwagę wnioski, osiągnął w pewnym stopniu swój cel, gdyż przyczynił się
                                                do zacieśnienia więzów przyjaźni. Nie został on jednak uwzględniony w
                                                przyjętej numeracji.</p>

                                            <p>W dniu 21 maja 1927 roku odbyło się w Warszawie V doroczne Walne Zebranie
                                                Koła Wychowańców b. Szkoły Realnej w Łowiczu. Prezes Koła profesor
                                                Politechniki Warszawskiej Mieczysław Bronikowski wezwał obecnych do
                                                uczczenia przez powstanie zmarłych w ostatnim roku kolegów. Następnie
                                                wybrano mec. Lucjana Parzyńskiego na przewodniczącego zebrania.
                                                Zastępcami zostali Józef Rosiński i Józef Sokołowski, sekretarzem Michał
                                                Grundwald. Głównym punktem była sprawa urządzenia Zjazdu Koleżeńskiego w
                                                Łowiczu. Wydana została odpowiednia odezwa.</p>

                                            <p>W dniu 19 czerwca 1927 roku odbył się Zjazd b. Wychowańców Szkoły Realnej
                                                Łowickiej od najdawniejszych czasów oraz maturzystów Gimnazjum
                                                Państwowego im. Ks. Józefa Poniatowskiego. Przybywający goście witani
                                                byli na dworcu kolejowym przez orkiestrę szkolną z dyrektorem Edwardem
                                                Biegańskim na czele. W uroczystym pochodzie przez miasto, dotarli
                                                wszyscy pod pomnik „Bojownikom o niepodległość”. Po złożeniu wieńca
                                                udali się do kościoła Ojców Pijarów. Po mszy świętej uczestnicy Zjazdu
                                                zwiedzili Muzeum im. Władysława Tarczyńskiego, Magistrat, gdzie wpisano
                                                się do księgi pamiątkowej i plantacje miejskie. W Księdze Pamiątkowej
                                                widnieją 33 podpisy. Wspólny obiad przygotowano w Klubie Obywatelsko –
                                                Urzędniczym. W trakcie posiłku wygłaszano przemówienia i wznoszono
                                                toasty. Zebrano także ponad tysiąc złotych, przeznaczając je na bursę
                                                „Bartoszków”. Czas upłynął na miłej wspólnej pogawędce. Następne
                                                spotkanie zaplanowano za pięć lat.</p>


                                            <h4 class="paddingTop-40 paddingBottom-10">IV Zjazd Koleżeński (1932)</h4>

                                            <p>Stosunkowo najmniej informacji zachowało się o Zjeździe, który odbył się
                                                w dniu 19 czerwca 1932 roku. W odezwie Koła Wychowańców b. Szkoły
                                                Realnej, skierowanej do swoich członków czytamy: „W myśl uchwały IX
                                                Walnego Zebrania, odbytego w r. 1931, Zarząd Koła organizuje III Ogólny
                                                Zjazd koleżeński w Łowiczu w dniu 19 czerwca 1932 r. z okazji 10-lecia
                                                istnienia Koła”. Prawdopodobnie w okresie ostatnich pięciu lat ustalono,
                                                że numeracja zjazdów liczona będzie od momentu powstania Koła tj. od
                                                1922 roku. Nie ma jednak na to dowodu. Nie wiadomo też na jakiej
                                                podstawie Henryk Nałęcki w swej informacji „Wiadomości o Zjazdach
                                                Koleżeńskich b. wych. i wych. szkół średnich w Łowiczu” z dnia 8 lutego
                                                1976 roku, oznaczył ten Zjazd numerem IV. Program Zjazdu przedstawiał
                                                się następująco. Przybycie wszystkich uczestników nastąpiło przed
                                                godziną dziesiątą. Nabożeństwo odprawione zostało o 10.30 w kościele
                                                Ojców Pijarów. Po mszy złożono kwiaty pod tablicą poległych w obronie
                                                Ojczyzny uczniów Państwowego Gimnazjum im Ks. J. Poniatowskiego.</p>

                                            <p>Następnym punktem programu był udział u uroczystym rozdaniu matur
                                                abiturientom z 1932 roku. O godzinie 14 przedstawione zostało
                                                sprawozdanie z dziesięcioletniej działalności Koła. Prawdopodobnie
                                                wygłosił je Prezes Koła prof. Mieczysław Bronikowski. Po części
                                                oficjalnej wszyscy uczestnicy udali się na wspólny obiad. Po posiłku ,
                                                około godziny 16 nastąpiło zwiedzanie Muzeum Ziemi Łowickiej im.
                                                Władysława Tarczyńskiego, Muzeum Łowickiego Oddziału Towarzystwa
                                                Krajoznawczego oraz miasta. W godzinach wieczornych uczestnicy
                                                rozjechali się w swoje strony.</p>


                                            <h4 class="paddingTop-40 paddingBottom-10">V Zjazd Koleżeński (1938)</h4>

                                            <p>W dniu 8 czerwca 1924 roku zostało zawiązane Koło Wychowanek Żeńskiej
                                                Szkoły Łowickiej. Lakoniczna informacja podaje, iż celem działalności
                                                Koła było podtrzymywanie więzów koleżeńskich. Pierwsze zebranie
                                                założycielskie odbyło się w niedzielę 15 czerwca o godzinie 11. Niewiele
                                                zachowało się informacji o działalności tego Koła. Pierwszy zjazd
                                                koleżeński na terenie gimnazjum żeńskiego zaplanowano na 11 czerwca 1933
                                                roku. Z uwagi na ograniczenie się do maturzystek z 1927 roku, należałoby
                                                przychylić się do określenia – spotkanie koleżeńskie. Celem tego
                                                spotkania miało być utrzymanie łączności i zacieśnienie stosunków
                                                koleżeńskich po opuszczeniu szkoły.</p>

                                            <p>Z okazji piętnastej rocznicy upaństwowienia Gimnazjum im. J. U.
                                                Niemcewicza na dzień 21 października 1934 roku zaplanowano Zjazd b.
                                                Wychowanek Gimnazjum Żeńskiego w Łowiczu. Porządek Zjazdu przedstawiał
                                                się następująco: zbiórka w gimnazjum żeńskim o godzinie 10.15. Msza
                                                święta odprawiona została w kaplicy szkolnej o godzinie 10.30. Po mszy
                                                złożono wieniec na grobie przełożonej ś. p. Bronisławy
                                                Bronikowskiej.</p>

                                            <p>Uroczyste otwarcie Zjazdu nastąpiło o godzinie 12.30. Po wspólnie
                                                spożytym obiedzie kontynuowano dalej obrady. W kasynie 10 P. P. o
                                                godzinie 20 rozpoczął się Dancing – Bridge. Do tańca przygrywała
                                                orkiestra wojskowa. Dochód przeznaczony został na kolonie letnie dla
                                                uczennic gimnazjum żeńskiego.</p>

                                            <p>W marcu 1933 roku istniał już wspólny Zarząd Koła b. Wychowanek i
                                                Wychowańców Średnich Szkół Łowickich. Następny Zjazd został już
                                                zorganizowany wspólnie. Związek Propagandy Turystycznej Ziemi Łowickiej
                                                wydał program Zjazdu Wychowanków Szkół Łowickich. Odbył się on 21
                                                czerwca 1936 roku. Przybyłych gości witano na dworcu kolejowym. O
                                                godzinie 10.15 nastąpiła zbiórka na dziedzińcu gimnazjum żeńskiego, po
                                                czym wszyscy uczestnicy przeszli na mszę do kościoła Ojców Pijarów. O
                                                godzinie 12 złożono wieńce pod tablicą uczniów szkół poległych w roku
                                                1920 i pod tablicą Józefa Chełmońskiego, Wielkiego Syna Ziemi Łowickiej.
                                                Następnie uczestniczono w uroczystościach szkolnych w Gimnazjum Męskim.
                                                O godzinie 13.30 nastąpiło właściwe otwarcie i obrady Zjazdu Wychowanków
                                                Szkół Łowickich. Podjęto uchwałę zmierzającą do ufundowania tablicy
                                                pamiątkowej ku czci profesora historii architektury na Politechnice
                                                Warszawskiej i artysty Stanisława Noakowskiego, który w latach 1884-1886
                                                uczęszczał do Szkoły Realnej w Łowiczu. Wmurowanie tablicy na frontowej
                                                ścianie budynku gimnazjalnego i jej odsłonięcie miałoby nastąpić w dniu
                                                V Zjazdu Koleżeńskiego w czerwcu 1938 roku w dziesięciolecie śmierci
                                                Noakowskiego. Po przerwie obiadowej uczestnicy Zjazdu w zależności od
                                                zainteresowania mogli uczestniczyć w wielu przygotowanych imprezach. W
                                                gmachu Gimnazjum Męskiego zorganizowano wystawę „Przemysł i zdobnictwo
                                                Księżackie”, w budynku należącym do elektrowni wystawę „Łowicz w
                                                Wykresach”. Wystawa rzemieślnicza zorganizowana została w Szkole
                                                Podstawowej im. Prezydenta Mościckiego. Cały dzień otwarte były muzea
                                                Miejskie im. Tarczyńskiego i Etnograficzne P.T.K.. Można było zwiedzać
                                                miasto, kościoły, zabytki. Zorganizowano wycieczkę do Arkadii. W budynku
                                                gimnazjum męskiego urządzono zabawę taneczną. Kino „Eos” wyświetlało
                                                film pt. „Niewidzialny Promień”, natomiast w „Corso” prezentowano film
                                                „Sing – Sing”. Dla chętnych, którzy chcieli spędzić czas nad rzeką Bzurą

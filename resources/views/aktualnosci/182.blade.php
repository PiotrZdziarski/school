
Uczniowie klasy 2b wybrali się 23 maja 2018 r. na spektakl „Wszystko w rodzinie” do Teatru Powszechnego do Łodzi. Sztuka Raya Cooney’a rozgrywa się w jednym z londyńskich szpitali, gdy  spokojne życie doktora Davida Mortimore’a zostaje zakłócone przez wizytę znajomej sprzed lat. Spirala komicznych sytuacji zdaje się nie mieć końca.

<p class="paddingTop-20">Teatralna farsa stanowiła okazję do dobrej zabawy dla widzów, w tym sympatycznych uczniów klasy 2b z wychowawcą p. Markiem Rybickim i p. Aleksandrą Wojciechowską-Gasik.</p>





<p>15 sierpnia 2018 roku młodzież naszej szkoły wzięła udział w uroczystościach upamiętniających rocznicę Bitwy
    Warszawskiej.</p>

<p>Zgodnie z wieloletnią już tradycją nasze Liceum wystawiło poczty sztandarowe aby uczcić 98. rocznicę bitwy zwanej
    „Cudem nad Wisłą”.</p>

<p>Przed pomnikiem Józefa Piłsudskiego wysłuchaliśmy wypowiedzi Burmistrza Miasta Pana Krzysztofa Jana Kalińskiego na
    temat samej rocznicy oraz ustanowienia Święta Wojska Polskiego właśnie w dniu 15 sierpnia.</p>

<p>Złożone zostały kwiaty pod pomnikiem Marszałka a następnie w asyście Orkiestry Miasta Łowicz przemaszerowaliśmy do
    bazyliki katedralnej na mszę za ojczyznę.</p>

<p>Poczty wystawiła klasa 3a pod opieką wychowawczyni pani Eweliny Kapusty.</p>



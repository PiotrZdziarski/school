<p>Do 09 czerwca uczniowie naszej szkoły mieli możliwość zobaczenia ciekawej wystawy przygotowanej przez pracowników Oddziałowego Biura Edukacji Publicznej IPN w Łodzi, pt. „Kalendarium Łodzi 1939–1989. Od czterech kultur do monokultury”.To 50 paneli zdjęć i opisów przedstawiających najważniejsze wydarzenia polityczne, kulturalne i gospodarcze Łodzi lat 1939–1989. Młodzież mogła zapoznać się z wieloma ważnymi postaciami i wydarzeniami dotyczącymi historii miasta, np. strajki i demonstracje pracowników łódzkich zakładów, historia Kościoła i opozycji,  katastrofy i  zbrodnie kryminalne, którymi żywo interesowali się łodzianie.</p>
<p>Mieszkańcy Łowicza także mieli okazję zobaczyć ekspozycję w naszej szkole podczas Małego Pikniku Naukowego, który odbył się w sobotę 09.06.2018 r.</p>




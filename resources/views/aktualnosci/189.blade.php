<p>29 maja 2018 r. uczniowie zainteresowani fizyką uczestniczyli na Wydziale Elektrycznym Politechniki Warszawskiej w pokazach i wykładach z zakresu elektryczności (28 uczniów z klasy 2b i 4 z klasy 2a).</p>
<p>Dzięki uprzejmości pana dra inż. Tadeusza Daszczyńskiego, pracownika naukowego Zakładu Aparatów i Automatyki Elektroenergetycznej Instytutu Elektroenergetyki Politechniki Warszawskiej, Absolwenta Naszego Liceum, obserwowaliśmy pokazy wyładowań elektrycznych w Laboratorium Wysokich Napięć. Poznaliśmy własności łuku elektrycznego i wiemy, czy prąd płynie zawsze najkrótszą drogą, skąd biorą się trzaski pod liniami wysokiego napięcia. Widowiskowe pokazy za pomocą generatora udarów napięciowych rzędu 2 milionów voltów i pioruny pozostaną w naszej pamięci.</p>
<p>Pan dr inż. Tadeusz Daszczyński oprowadził nas po Laboratorium Zwarciowym, gdzie obserwowaliśmy zjawiska związanie z przepływem prądów o ogromnych natężeniach, oglądaliśmy obrazy z kamery termowizyjnej. Z kolei w Laboratorium Elektroenergoniki widzieliśmy roboty przemysłowe programowane przez studentów Wydziału Elektrycznego na kierunku robotyka i automatyka. W Laboratorium Elektroenergetyki mogliśmy obserwować urządzenia do przekształcania energii elektrycznej na przykładzie instalacji fotowoltaicznej.</p>
<p>W Laboratorium napędów i maszyn elektrycznych przedstawiono nam silniki indukcyjne, model silnika liniowego z bezstykowym ładowaniem na torze badawczym.</p>
<p>Panu dr. inż. Tadeuszowi Daszczyńskiemu za wyjątkową lekcję – wykład i pokazy – bardzo dziękuje wychowawca klasy 2b i nauczyciel fizyki p. Marek Rybicki oraz młodzież – uczestnicy wycieczki, być może przyszli studenci Politechniki Warszawskiej.</p>


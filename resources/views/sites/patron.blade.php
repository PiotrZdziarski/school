                                Chełmoński uczył się malarstwa w warszawskiej Szkole Rysunkowej pod kierunkiem Wojciecha Gersona, który wielką wagę przywiązywał do oglądania i studiowania natury i zachęcał do tego swoich uczniów. Żył w biedzie, mieszkał z kolegami w ubogiej izbie na Powiślu, ale zawsze miał rozmach w malowaniu, humor, fantazję. Jego przygody przygody posłużyły Henrykowi Sienkiewiczowi do napisania zabawnego opowiadania o malarzach pt. „Ta trzecia”.

                                Z Warszawy wyjechał Chełmoński na dwa lata do Monachium, a stamtąd, już jako dojrzały malarz, przeniósł się do Paryża. I za granicą artysta z początku biedował. Potem jednak zyskał ogromne uznanie. Francuzi, Amerykanie płacili po kilkanaście tysięcy franków za jeden jego obraz, za „Trójki” i „Czwórki”, zaprzęgi koni ciągnących sanie, które widział w czasie swej poprzedniej bytności na Ukrainie. W Paryżu namalował też obraz „Noc na Ukrainie” i „Przed karczmą”.


<p>Pomyśl ile to już lat idziemy przez ten świat…</p>

<p>Te słowa, które znalazły się na zaproszeniu, świetnie odzwierciedlały charakter wydarzenia, którego uczestnikami były
    dwa pokolenia uczniów naszego liceum.</p>

<p>Dnia 12 czerwca 2018 roku odbyła się uroczystość zorganizowana przez absolwentów naszego liceum, którzy dokładnie 65
    lat temu przystąpili do egzaminu dojrzałości.</p>

<p>Spotkanie to nie miało jedynie na celu celebrowania rocznicy matury oraz spotkania z koleżankami i kolegami ze
    szkolnej ławki. Była to także okazja, by podziękować tym, którzy włożyli ogromny wkład w edukację maturzystów 1953 i
    tym, którzy dbają o to, aby historia łowickich seniorów zachowała się w świadomości ludzi. Głównym organizatorem
    wydarzenia był Pan Janusz Grundwald.</p>

<p>Obchody rozpoczęły się mszą świętą w kościele Sióstr Bernardynek, którą odprawił jeden z absolwentów – Ksiądz Kanonik
    Brunon Dąbrowski. Obecna była również klasa Id wraz ze Sztandarem Szkoły.</p>

<p>Po mszy świętej obchody rocznicy przeniosły się do Ratusza, gdzie odbyło się oficjalne spotkanie z zaproszonymi
    gośćmi, wśród których znalazło się 3 dyrektorów I LO – pan Henryk Zasępa, pani Elżbieta Skoneczna oraz pani Dorota
    Dziekanowska a także dyrektor łowickiego Muzeum pani Marzena Kozanecka-Zwierz oraz – prezes Koła Wychowanków i
    Wychowanek pani Jolanta Wróbel.</p>

<p>Niepowtarzalną okazję do tego, by posłuchać wspomnień, które zmuszały do refleksji i anegdot o szkolnych czasach
    absolwentów mieli również uczniowie klasy Id, którzy zostali zaproszeni do Urzędu Miasta. Było to dla wszystkich
    bardzo interesujące, pouczające, a przede wszystkim wzruszające spotkanie.</p>

<p>Na zakończenie uroczystości nie mogło zabraknąć podziękowań, wpisów zebranych do Księgi Pamiątkowej oraz życzeń
    zdrowia, do których dołącza się cała społeczność uczniowska I Liceum Ogólnokształcącego im. Józefa Chełmońskiego w
    Łowiczu.</p>




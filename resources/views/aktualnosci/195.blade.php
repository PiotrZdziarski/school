<p>11 czerwca 2018 roku młodzież klas drugich rozszerzająca biologię i geografię uczestniczyła w bardzo ciekawych warsztatach .</p>

<p>W ramach wycieczki programowej do Arboretum SGGW w Rogowie młodzież zainteresowana naukami przyrodniczymi biologia i geografią wzięła udział w warsztatach z zakresu dendrologii. Uczniowie uczyli się rozpoznawać drzewa i krzewy pochodzące z różnych stref klimatycznych.</p>

<p>Największym zainteresowaniem cieszył się cis pospolity, magnolia wielkolistna i niesamowita dereń kousa. Nie obeszło się również bez pytań o: kwiaty kasztana jadalnego, tulipanowca amerykańskiego i  przywarki japońskiej. Wielu z nas zachwycało się dopiero co zakwitającymi liliami wodnymi w Alpinarium.</p>

<p>Kolejnym punktem programu była lekcja o bezkręgowcach. Młodzież cieszyła się możliwością wyławiania i dokładnego oglądania pluskolców, larw jętek, ośliczek i innych bezkręgowców wodnych. Wycieczka zakończyła się w miłej atmosferze.</p>

<p>Za organizację całego przedsięwzięcia dziękujemy naszym nauczycielom: pani Urszuli Tworek, pani Agnieszce Piorun i wychowawczyni pani Małgorzacie Kutkowskiej.</p>

<p>Opracowały: Martyna Wojtczak i Wioletta Snopek z kl. 2c</p>




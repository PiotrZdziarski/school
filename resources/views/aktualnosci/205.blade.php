<p>W dniu 1 sierpnia 2018 r. uczniowie naszej szkoły (z klasy 1b) wraz z pocztem sztandarowym AK i przedstawiciele Klubu
    Historycznego im. AK uczcili rocznicę wybuchu Powstania Warszawskiego. Uroczystości związane z obchodami odbyły się
    na Cmentarzu Katedralnym przy pomniku Szarych Szeregów. Burmistrz Miasta pan Krzysztof Jan Kaliński krótko
    przypomniał historię 63 dni heroicznej walki o wolną Polskę, walki toczonej przez żołnierzy Armii Krajowej oraz
    cywilów – mieszkańców stolicy. Podziękował wszystkim zebranym i pocztom sztandarowym oraz zapewnił, że łowiczanie
    nigdy nie zapomną tego bohaterskiego zrywu, a idea upamiętniania powstańców warszawskich będzie kontynuowana, nawet
    kiedy ostatni z walczących odejdzie na wieczną wartę.</p>

<p>Opiekunami młodzieży byli p. J. Skoneczna-Sałuda i p. P. Salamondra.</p>


